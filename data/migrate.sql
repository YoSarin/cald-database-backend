CREATE TABLE IF NOT EXISTS :new_schema_name:.tournament (
    id int AUTO_INCREMENT PRIMARY KEY,
    name varchar(255),
    date DATETIME,
    season_id int,
    location varchar(255),
    duration int,
    deleted boolean DEFAULT false,
    FOREIGN KEY (season_id) REFERENCES season(id)
